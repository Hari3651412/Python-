
print(float(20))  # Integer to float: 20.0

y = 15.7
print(int(y))  # Float to integer: 15

# String to number conversions
s = "25"
print(int(s))  # String to integer: 25
print(float(s))  # String to float: 25.0

# Number to string conversion
z = 100
print(str(z))  # Number to string: "100"
print(type(str(z)))  # Check type: <class 'str'>
